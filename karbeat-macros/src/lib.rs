/// Helper struct for parsing key=value pairs in #[param(...)]
struct ParamAttr {
    id: Option<u32>,
    default: Option<f32>,
    min: Option<f32>,
    max: Option<f32>,
}

impl Parse for ParamAttr {
    fn parse(input: ParseStream) -> syn::Result<Self> {
        let mut id = None;
        let mut default = None;
        let mut min = None;
        let mut max = None;

        let pairs: Punctuated<syn::MetaNameValue, Token![,]> = Punctuated::parse_terminated(input)?;

        for pair in pairs {
            let key = pair
                .path
                .get_ident()
                .ok_or_else(|| syn::Error::new_spanned(&pair.path, "expected identifier"))?
                .to_string();

            let value = &pair.value;

            match key.as_str() {
                "id" => {
                    if let Expr::Lit(expr_lit) = value {
                        if let Lit::Int(lit_int) = &expr_lit.lit {
                            id = Some(lit_int.base10_parse()?);
                        }
                    }
                }
                "default" => {
                    default = Some(parse_f32_expr(value)?);
                }
                "min" => {
                    min = Some(parse_f32_expr(value)?);
                }
                "max" => {
                    max = Some(parse_f32_expr(value)?);
                }
                _ => {}
            }
        }

        Ok(ParamAttr {
            id,
            default,
            min,
            max,
        })
    }
}

fn parse_f32_expr(expr: &Expr) -> syn::Result<f32> {
    match expr {
        Expr::Lit(expr_lit) => match &expr_lit.lit {
            Lit::Float(lit_float) => lit_float.base10_parse(),
            Lit::Int(lit_int) => {
                let val: i64 = lit_int.base10_parse()?;
                Ok(val as f32)
            }
            _ => Err(syn::Error::new_spanned(
                expr,
                "expected float or int literal",
            )),
        },
        Expr::Unary(unary) if matches!(unary.op, syn::UnOp::Neg(_)) => {
            let inner = parse_f32_expr(&unary.expr)?;
            Ok(-inner)
        }
        _ => Err(syn::Error::new_spanned(expr, "expected numeric literal")),
    }
}

/// Derive macro for generating parameter helper methods from #[param(...)] attributes.
/// Use for synth-specific parameters beyond the base set.
#[proc_macro_derive(KarbeatParams, attributes(param))]
pub fn derive_karbeat_params(input: TokenStream) -> TokenStream {
    let input = parse_macro_input!(input as DeriveInput);
    let name = &input.ident;

    let mut set_arms = Vec::new();
    let mut get_arms = Vec::new();
    let mut default_inserts = Vec::new();

    if let Data::Struct(data) = &input.data {
        if let Fields::Named(fields) = &data.fields {
            for field in &fields.named {
                let field_name = field.ident.as_ref().unwrap();

                for attr in &field.attrs {
                    if attr.path().is_ident("param") {
                        let param_attr: ParamAttr = match attr.parse_args() {
                            Ok(p) => p,
                            Err(_) => continue,
                        };

                        if let (Some(id), Some(default)) = (param_attr.id, param_attr.default) {
                            let set_logic =
                                if let (Some(min), Some(max)) = (param_attr.min, param_attr.max) {
                                    quote! { self.#field_name = value.clamp(#min, #max); }
                                } else {
                                    quote! { self.#field_name = value; }
                                };
                            set_arms.push(quote! { #id => { #set_logic } });
                            get_arms.push(quote! { #id => self.#field_name });
                            default_inserts.push(quote! { map.insert(#id, #default); });
                        }
                    }
                }
            }
        }
    }

    quote! {
        impl #name {
            pub fn auto_set_parameter(&mut self, id: u32, value: f32) {
                match id {
                    #(#set_arms,)*
                    _ => {}
                }
            }

            pub fn auto_get_parameter(&self, id: u32) -> f32 {
                match id {
                    #(#get_arms,)*
                    _ => 0.0
                }
            }

            pub fn auto_default_parameters(&self) -> std::collections::HashMap<u32, f32> {
                let mut map = std::collections::HashMap::new();
                #(#default_inserts)*
                map
            }
        }
    }
    .into()
}
